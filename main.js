function calculateNumbers(a, b) {
    if (a % 2 === 0 && b % 2 === 0) {
        return a * b;
    } else if (a % 2 !== 0 && b % 2 !== 0) {
        return a + b;
    } else if (a % 2 !== 0 && b % 2 === 0) {
        return a;
    } else {
        return b;
    }
}

